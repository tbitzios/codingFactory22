package gr.aueb.cf.ch3;

/**
 * Prints char values
 */
public class CharPrintApp {
    public static void main(String[] args) {
        char ch1='!';
        char ch2='S';

        System.out.printf("char: %c, ordinal: %d\n", ch1, (int) ch1);
        System.out.printf("char: %c, ordinal: %d\n", ch2, (int) ch2);

    }
}
